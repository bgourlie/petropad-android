package com.wbg;

public class PetropadConstraintException extends Exception {

    public PetropadConstraintException() {
    }

    public PetropadConstraintException(Throwable cause) {
        super(cause);
    }

    public PetropadConstraintException(String message) {
        super(message);
    }

    public PetropadConstraintException(String message, Throwable cause) {
        super(message, cause);
    }
}
